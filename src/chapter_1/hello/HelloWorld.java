package chapter_1.hello; // packages must be at the top

// imports - need basis

// any file - only public class
// public class name should be file's name
public class HelloWorld {

    // main method - always first method to execute
    // <access modifier> <return type> <method name> ( <parameters>) {}
    public static void main(String[] args) { // block of code - start
        // statements - single line of code - they always ends with semicolon
        System.out.println("Hello World!, \n"); // \n is an escape sequence (\)
        System.out.println("Hello World!!");
        System.out.println("Hello World!!");
    } // block of code - end
}